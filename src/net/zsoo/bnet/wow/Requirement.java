package net.zsoo.bnet.wow;

public class Requirement {
	private SimpleValue level;
	private Skill skill;

	public SimpleValue getLevel() {
		return level;
	}

	public void setLevel(SimpleValue level) {
		this.level = level;
	}

	public Skill getSkill() {
		return skill;
	}

	public void setSkill(Skill skill) {
		this.skill = skill;
	}
}
